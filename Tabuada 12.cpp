#include <iostream>
using namespace std;
//Crie um programa que imprima a tabuada do 12 em tela. Utilize o
//comando for para isso.
 int main(int argc, char** argv){
 	cout << "Tabuada do 12:"<< endl;
 	
 	for (int i = 0; i<=10;i++){
 		cout << i << "x 12 = " << (i*12) << endl;
	 }
	 system ("PAUSE");
	 return 0;
 }
 
