#include <iostream>
using namespace std;
int main(int argc, char** argv){
	//Crie um programa que imprima números menores ou iguais a 14. Utilize
	//o comando While para isso.
	int y = 14;
	cout << "Numeros de 0 ate 14" << endl;
	while (y >= 0){
		cout << y << endl;
		y--;
	}
	
	return 0;
}
